task autonomous()
{
    // Enable PID Control
    nMotorPIDSpeedCtrl[right1] = RegSpeed;
    nMotorPIDSpeedCtrl[left1] = RegSpeed;

    if (isNearFlag)
    {
        if (isPlatform)
            autonNearFlagPlat();
        else
            autonNearFlagNoPlat();
    }
    else
    {
        if (isPlatform)
            autonFarFlagPlat();
        else
            autonFarFlagNoPlat();
    }
}

