void pre_auton()
{
    // Set bStopTasksBetweenModes to false if you want to keep user created tasks
    // running between Autonomous and Driver controlled modes. You will need to
    // manage all user created tasks if set to false.
    bStopTasksBetweenModes = true;

    // Reset just in case
    resetMotorEncoder(right1);
    resetMotorEncoder(left1);

    // Initialize motor slaves
    slaveMotor(right0, right1);
    slaveMotor(right2, right1);
    slaveMotor(left0, left1);
    slaveMotor(left2, left1);

    slaveMotor(flipL, flipR);

    // Initialize the gyro
    gyro_init(gyro, gyroSense, false);
    pidInit(gyroPID, kP, 0, kD);

    // Initialize driving straight PID
    pidInit(motorPositionPID, 0.005, 0, 0);
    pidInit(keepStraightPID, 0.01, 0, 0);

    // Initialize flipPID
    pidInit(flipPID, 0.1, 1, 0.01);
    keepHolding = false;

    // Choose auton program
    // Choose blue or red alliance
    if (SensorValue[blueRedJumper] == 1)
    {
        turnDirection = 1;
        SensorValue[blueALED] = 1;
    }
    else
    {
        turnDirection = -1;
        SensorValue[redALED] = 1;
    }

    // Choose if is near flag
    if (SensorValue[nearFarJumper] == 1)
        isNearFlag = true;
    else
    {
        isNearFlag = false;
        SensorValue[farFlagLED] = 1;
    }

    // Choose if doing platform
    if (SensorValue[platJumper] == 1)
    {
        isPlatform = true;
    }
    else
    {
        isPlatform = false;
        SensorValue[noPlatLED] = 1;
    }
}

void
holdFlipAngle(float fTarget)
{
    while (keepHolding)
    {
        // Get current position
        float fFlipAngle = SensorValue[flipSense];

        // Calculate the power and apply it
        float fPower = pidCalculate(flipPID, fTarget, fFlipAngle) * 127;
        motor[flipR] = fPower;

        wait1Msec(25);
    }
}
